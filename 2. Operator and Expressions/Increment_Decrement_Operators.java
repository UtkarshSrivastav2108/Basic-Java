public class Increment_Decrement_Operators {

    public static void main(String[] args) {

        int x1 = 5;
        x1++;

        int x2 = 15, y;
        y = ++x2;
        y = x2++;
        char x = 'A';
        x++;
        System.out.println(x);
        System.out.println(x1);
        System.out.println(y);
        System.out.println(x2);
    }
}
